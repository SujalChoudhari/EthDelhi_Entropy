// Helper function to apply theme
export function applyTheme(themeId: string, isDark: boolean) {
  const theme = themes.find((t) => t.id === themeId);
  if (!theme) {
    console.warn(`Theme with id "${themeId}" not found.`);
    return;
  }

  const variables = isDark ? theme.variables.dark : theme.variables.light;
  const root = document.documentElement;

  if (!variables) {
    console.warn(
      `Variables for ${isDark ? "dark" : "light"} mode not found in theme "${themeId}".`,
    );
    return;
  }

  Object.entries(variables).forEach(([key, value]) => {
    if (value !== undefined) {
      root.style.setProperty(`--${key}`, value);
    } else {
      console.warn(
        `Variable "--${key}" is undefined in theme "${themeId}" (${isDark ? "dark" : "light"
        } mode).`,
      );
    }
  });
}
